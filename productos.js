let categoria;
let categoriaNum = 0;
let marca = 0;
let producto = 0;
let productosMostrar = document.getElementById("elementos__elegidos");

function definirCategoria(categoriaNum) {
  switch (categoriaNum) {
    case 0:
      categoria = "";
      return categoria;
      break;
    case 1:
      categoria = "productos-nuevos";
      return categoria;
      break;
    case 2:
      categoria = "nuestras-ofertas";
      return categoria;
      break;
    case 3:
      categoria = "mas-vendido";
      return categoria;
      break;
    case 4:
      categoria = "mas-buscado";
      return categoria;
      break;
  }
}

for (let i = 0; i < images.length; i++) {
  let image = images[i];
  let imageElement = document.createElement("img");
  imageElement.src = image.src;

  let titleElement = document.createElement("h2");
  titleElement.textContent = image.title;

  let descriptionElement = document.createElement("p");
  descriptionElement.textContent = image.description;

  let quantityElement = document.createElement("p");
  quantityElement.textContent = "Cantidad: " + image.quantity;

  let priceElement = document.createElement("p");
  priceElement.textContent = "Precio: " + image.price;

  let imageContainer = document.createElement("div");
  imageContainer.appendChild(imageElement);
  imageContainer.appendChild(titleElement);
  imageContainer.appendChild(descriptionElement);
  imageContainer.appendChild(quantityElement);
  imageContainer.appendChild(priceElement);

  productosMostrar.appendChild(imageContainer);
}

function mostrarElementosPorCategoriaYMarca(categoria = "", marca = "") {
  productosMostrar.innerHTML = ""; // Limpia el contenedor antes de mostrar los elementos filtrados

  for (let i = 0; i < images.length; i++) {
    let image = images[i];

    // Comprueba si la categoría y la marca del objeto coinciden con las seleccionadas (ignora el filtro si el parámetro está vacío)
    if (
      (categoria === "" || image.category === categoria) &&
      (marca === "" || image.brand === marca)
    ) {
      let imageElement = document.createElement("img");
      imageElement.src = image.src;

      let titleElement = document.createElement("h2");
      titleElement.textContent = image.title;

      let descriptionElement = document.createElement("p");
      descriptionElement.textContent = image.description;

      let quantityElement = document.createElement("p");
      quantityElement.textContent = "Cantidad:" + image.quantity;

      let priceElement = document.createElement("p");
      priceElement.textContent = "Precio: " + image.price;

      let imageContainer = document.createElement("div");
      imageContainer.appendChild(imageElement);
      imageContainer.appendChild(titleElement);
      imageContainer.appendChild(descriptionElement);
      imageContainer.appendChild(quantityElement);
      imageContainer.appendChild(priceElement);

      productosMostrar.appendChild(imageContainer);
    }
  }
}

